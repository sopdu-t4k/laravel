     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'title' => ['required', 'string'],
            'preview' => ['required', 'string'],
            'source' => ['required', 'string'],
	]);

        return response()->json($request->only(['title', 'preview', 'source', 'status']), 201);
    }

